package codelagos;

/*
* Working with variable
* */
public class Variables {
    //These are instance variable or field, the field fields the property of this class.
    private int fVal1 = 190;
    private int fVal2 =32;

    public static void main(String[] args) {
        int var1, var2;         // Declares three ints, a, b, and c.
        var1 = 10;
        var2 = 10;              // Example of initialization
        byte B = 22;         // initializes a byte type variable B.
        double pi = 3.14159; // declares and assigns a value of PI.
        char a = 'a';        // the char variable a iis initialized with value 'a'
        // The variable var1, var2, B, pi, and a are example of local variables
        System.out.println(var1);
        System.out.println(var2);
        System.out.println(B);
        System.out.println(pi);
        System.out.println(a);
    }

}
